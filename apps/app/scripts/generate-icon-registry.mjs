#!/usr/bin/env node
// Reads SVG files for every slug referenced in @sporlo/integrations CATALOG
// and writes apps/app/src/lib/integration-icon-registry.ts. The registry is
// statically importable at build time so logos render without external CDN
// calls (cdn.simpleicons.org is unreliable in some regions).

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const APP_ROOT = join(__dirname, "..");
const REPO_ROOT = join(APP_ROOT, "..", "..");
const ICONS_DIR = join(APP_ROOT, "node_modules", "simple-icons", "icons");
const OUT_FILE = join(APP_ROOT, "src", "lib", "integration-icon-registry.ts");

// Parse the TS catalog as text and pull out simple_icon slugs.
const catalogPath = join(REPO_ROOT, "packages", "integrations", "src", "catalog.ts");
const catalog = readFileSync(catalogPath, "utf-8");
const slugs = new Set();
const re = /simple_icon:\s*"([a-z0-9-]+)"/g;
let m;
while ((m = re.exec(catalog)) !== null) {
  slugs.add(m[1]);
}

const registry = {};
const missing = [];
for (const slug of [...slugs].sort()) {
  const file = join(ICONS_DIR, `${slug}.svg`);
  if (!existsSync(file)) {
    missing.push(slug);
    continue;
  }
  const svg = readFileSync(file, "utf-8");
  const pathMatch = svg.match(/<path d="([^"]+)"/);
  if (!pathMatch) {
    missing.push(slug);
    continue;
  }
  registry[slug] = pathMatch[1];
}

const body = `// AUTO-GENERATED by scripts/generate-icon-registry.mjs — do not edit by hand.
// Run \`node scripts/generate-icon-registry.mjs\` after updating the catalog.
//
// Each entry is the SVG path data for a 24x24 viewBox icon. The IntegrationLogo
// component renders these inline so logos load instantly with no external CDN
// dependency.

export const INTEGRATION_ICONS: Record<string, string> = ${JSON.stringify(registry, null, 2)};
`;

writeFileSync(OUT_FILE, body, "utf-8");
console.log(`Wrote ${Object.keys(registry).length} icons to ${OUT_FILE}`);
if (missing.length > 0) {
  console.warn(`Missing from simple-icons (will fall back to gradient avatar):`);
  for (const slug of missing) console.warn(`  - ${slug}`);
}
